# Escribe un programa en python que convierta las
# siguientes temperaturas de grados Celsius a
# grados Fahenheit:
#    30 °C
#    -273.99 °C
#    100 °C

Celsius = 30
Fahenheit = (Celsius*1.8)+32
print("30 °C =", Fahenheit,"°F")

Celsius = -273.99
Fahenheit = (Celsius*1.8)+32
print("-273.99 °C =", Fahenheit,"°F")

Celsius = 100
Fahenheit = (Celsius*1.8)+32
print("100 °C =", Fahenheit,"°F")